# 4. Live Data Fetching Logic
# ==========================================
@st.cache_data(ttl=3600) # Cache data for 1 hour to prevent API limits
def fetch_company_data(ticker_symbol):
    try:
        tk = yf.Ticker(ticker_symbol)
        info = tk.info

        # Extract Financials
        financials = {
            "Name": info.get("longName", ticker_symbol),
            "Sector": info.get("sector", "N/A"),
            "Industry": info.get("industry", "N/A"),
            "ROE (%)": round(info.get("returnOnEquity", 0) * 100, 2) if info.get("returnOnEquity") else "N/A",
            "ROA (%)": round(info.get("returnOnAssets", 0) * 100, 2) if info.get("returnOnAssets") else "N/A",
            "P/E Ratio": round(info.get("trailingPE", 0), 2) if info.get("trailingPE") else "N/A",
            "Market Cap": info.get("marketCap", "N/A")
        }

        # Extract Governance/Officers
        officers_raw = info.get("companyOfficers", [])
        officers_list = []
        for officer in officers_raw:
            officers_list.append({
                "Name": officer.get("name", "Unknown"),
                "Age": officer.get("age", "N/A"),
                "Title": officer.get("title", "N/A"),
                "Exercised Value": officer.get("exercisedValue", 0)
            })
        df_officers = pd.DataFrame(officers_list) if officers_list else pd.DataFrame()

        return financials, df_officers
    except Exception as e:
        return None, None

