        print(f"User [{user_id}] asked: {prompt}")

        st.session_state.messages.append({"role": "user", "content": prompt})
        with chat_container:
            with st.chat_message("user"):
                st.markdown(prompt)

            with st.chat_message("assistant"):
                if model:
                    with st.spinner("Analyzing..."):
                        # Build context from the live data
                        data_context = f"Company: {fin_data}\n\nGovernance Data:\n{df_officers.to_string() if df_officers is not None else 'N/A'}"

                        SYSTEM_PROMPT = f"""
                        You are an expert AI financial and corporate governance analyst.
                        The user is currently viewing the following live data for a specific stock:
                        {data_context}

                        Be concise, data-driven, and insightful. Base your analysis on the provided live data.
                        If the user asks about board demographics (age, roles), analyze the Governance Data provided.
                        """

                        full_prompt = SYSTEM_PROMPT + "\n\nChat History:\n"
                        for m in st.session_state.messages:
                            full_prompt += f"{m['role'].capitalize()}: {m['content']}\n"

                        try:
                            response = model.generate_content(full_prompt)
                            st.markdown(response.text)
                            st.session_state.messages.append({"role": "assistant", "content": response.text})
                        except Exception as e:
                            st.error(f"Error generating response: {e}. Please check your API key quota and validity.")
                else:
                    st.error("Please enter a valid Gemini API Key in the sidebar first.")

